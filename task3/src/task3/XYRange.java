package task3;
//xの動く範囲[x1, x2]と動く間隔xstep
//yの動く範囲[y1, y2]と動く間隔ystep の6つの数の組を表す．

public class XYRange {
 public double x1, x2, xstep, y1, y2, ystep;
 public XYRange(double x1, double x2, double xstep,
                double y1, double y2, double ystep) {
     this.x1 = x1; this.x2 = x2; this.xstep = xstep;
     this.y1 = y1; this.y2 = y2; this.ystep = ystep;
 }
}